/// Defines standard spacing constants based on an 8‑pt grid.
///
/// Use these values for padding, margin, and gaps to ensure
/// consistent spacing across the app.
class AppSpacing {
  AppSpacing._();

  static const double xxs = 4;  // 0.5 × 8
  static const double xs = 8;   // 1 × 8
  static const double s = 16;   // 2 × 8
  static const double m = 24;   // 3 × 8
  static const double l = 32;   // 4 × 8
  static const double xl = 40;  // 5 × 8
  static const double xxl = 48; // 6 × 8
}
